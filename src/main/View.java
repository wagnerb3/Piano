package main;

public class View {
	
	/**
	 * Height of Keyboard
	 */
	private double height = 545;
	
	/**
	 * Width of Keyboard
	 */
	private double width = 1024;

	/**
	 * Getter
	 * @return Height of Keyboard
	 */
	public double getHeight() {
		return height;
	}

	/**
	 * Getter
	 * @return Width of Keyboard
	 */
	public double getWidth() {
		return width;
	}

}
